def show_lidar_on_image(pc_velo, img, calib, img_width, img_height):
    ''' Project LiDAR points to image '''
    imgfov_pc_velo, pts_2d, fov_inds = get_lidar_in_image_fov(pc_velo,
                                                              calib, 0, 0, img_width, img_height, True)
    imgfov_pts_2d = pts_2d[fov_inds, :]
    imgfov_pc_rect = calib.project_velo_to_rect(imgfov_pc_velo)

    import matplotlib.pyplot as plt
    cmap = plt.cm.get_cmap('hsv', 256)
    cmap = np.array([cmap(i) for i in range(256)])[:, :3] * 255

    for i in range(imgfov_pts_2d.shape[0]):
        depth = imgfov_pc_rect[i, 2]
        color = cmap[int(640.0 / depth), :]
        cv2.circle(img, (int(np.round(imgfov_pts_2d[i, 0])), int(np.round(imgfov_pts_2d[i, 1]))),
                   2, color=tuple(color), thickness=-1)
    # Image.fromarray(img).show()
    return img


def show_lidar_on_depth_image(pc_velo, calib, img_width, img_height):
    ''' Project LiDAR points to depth map '''
    imgfov_pc_velo, pts_2d, fov_inds = get_lidar_in_image_fov(pc_velo,
                                                              calib, 0, 0, img_width, img_height, True)
    imgfov_pts_2d = pts_2d[fov_inds, :]
    imgfov_pc_rect = calib.project_velo_to_rect(imgfov_pc_velo)

    depth_map = np.zeros(shape=(img_height, img_width), dtype='uint8')

    for i in range(imgfov_pts_2d.shape[0]):
        depth = int(np.round(imgfov_pc_rect[i, 2]))
        depth_map[int(np.round(imgfov_pts_2d[i, 1])), int(np.round(imgfov_pts_2d[i, 0]))] = depth

    # Image.fromarray(img).show()
    return depth_map


